<h1>DigiPi Plus</h1>
<br>
<h2>Trusted QSL</h2>
<p>
    Trusted QSL can be opened by minimizing the active app in the VNC, right-clicking the grey background and selecting it from the pop-up menu.
<br>
<h2>OpenWebRX+</h2>
<p>
    In order to save OpenWebRX+ configuration, first press the "Save Configs" button in the Settings tab, then turn OFF the OpenWebRX service.
    Next time you start, the configurations should be saved.
<br>
<h2>Grid Tracker</h2>
<p>
    Grid Tracker can be opened from within the VNC by minimizing WSJT-X and then right-clicking on the grey background. A menu should appear showing Grid Tracker.
    Pressing the "Save Configs" button in the Settings tab is required on your FIRST run in order to create the necessary files, or an error will occur.
<br>
<h2>Xastir</h2>
<p>
    On the FIRST initial run, press the "Save Configs" button in the Settings tab. Xastir will need to be manually started by right-clicking the grey background 
    in the VNC and selecting it from the menu. Make all your changes to the configuration at this time.
<br>
<h2>JS8Spotter</h2>
<p>
    JS8Spotter can be opened by minimizing JS8Call in the VNC, right-clicking the grey background and selecting it from the pop-up menu.
